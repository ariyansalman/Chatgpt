class DispatchRoutingTest(_BaseCase):
    def test_legacy_key_type_returns_handled_false(self):
        """KEY/FILE are deliberately absent from the dispatch table so the
        legacy handlers/payment_handlers.py code path keeps handling them."""
        from database import ProductType
        product_id = self._make_product(ProductType.KEY)
        order_id, _item_id = self._make_order_with_item(product_id)
        result = self.ds.dispatch(order_id)
        self.assertFalse(result.handled)

    def test_legacy_file_type_returns_handled_false(self):
        from database import ProductType
        product_id = self._make_product(ProductType.FILE)
        order_id, _item_id = self._make_order_with_item(product_id)
        result = self.ds.dispatch(order_id)
        self.assertFalse(result.handled)

    def test_missing_order_returns_error(self):
        result = self.ds.dispatch(999_999)
        self.assertEqual(result.error, "Order not found")

    def test_order_with_no_items_returns_error(self):
        from database import get_db_session, Order, OrderStatus
        with get_db_session() as s:
            o = Order(user_id=self.user_id, total_amount=1.0, status=OrderStatus.PROCESSING)
            s.add(o)
            s.commit()
            order_id = o.id
        result = self.ds.dispatch(order_id)
        self.assertEqual(result.error, "Order has no items")

    def test_explicit_order_item_id_targets_correct_item_in_multi_item_order(self):
        """Regression coverage for the documented multi-item cart-checkout
        requirement: passing order_item_id must deliver THAT item, not
        always fall back to items[0]."""
        from database import ProductType, get_db_session, OrderItem, Order, OrderStatus

        product_a = self._make_product(ProductType.AUTO_GENERATED,
                                       type_config='{"mode": "code", "length": 8}')
        product_b = self._make_product(ProductType.MANUAL_DELIVERY)

        with get_db_session() as s:
            o = Order(user_id=self.user_id, total_amount=20.0, status=OrderStatus.PROCESSING)
            s.add(o)
            s.flush()
            item_a = OrderItem(order_id=o.id, product_id=product_a, quantity=1, price=10.0)
            item_b = OrderItem(order_id=o.id, product_id=product_b, quantity=1, price=10.0)
            s.add_all([item_a, item_b])
            s.commit()
            order_id, item_a_id, item_b_id = o.id, item_a.id, item_b.id

        result_b = self.ds.dispatch(order_id, order_item_id=item_b_id)
        self.assertTrue(result_b.queued)  # MANUAL_DELIVERY path, not AUTO_GENERATED

        with get_db_session() as s:
            item_a_row = s.query(OrderItem).filter_by(id=item_a_id).first()
        self.assertIsNone(item_a_row.delivered_asset, "untouched item must stay undelivered")

        from database.models import ManualDeliveryTask, GeneratedValue
        with get_db_session() as s:
            manual_tasks = s.query(ManualDeliveryTask).filter_by(order_id=order_id).all()
            generated = s.query(GeneratedValue).filter_by(order_id=order_id).all()
        self.assertEqual(len(manual_tasks), 1)
        self.assertEqual(manual_tasks[0].product_id, product_b)
        self.assertEqual(len(generated), 0, "the AUTO_GENERATED item must not have been dispatched")

    def test_unknown_order_item_id_returns_error(self):
        from database import ProductType
        product_id = self._make_product(ProductType.AUTO_GENERATED)
        order_id, _item_id = self._make_order_with_item(product_id)
        result = self.ds.dispatch(order_id, order_item_id=999_999)
        self.assertIn("not found", result.error)


