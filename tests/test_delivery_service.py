"""services/delivery_service.py — per-type deliverers, idempotent replay,
and top-level dispatch() routing.

Complements the reserve()->consume() round-trip coverage already in
tests/test_inventory_and_idempotency.py with the non-key-backed deliverers
(DOWNLOADABLE_FILE, AUTO_GENERATED, MANUAL_DELIVERY, SERVICE) and the
dispatch() routing/legacy-fallback behaviour.
"""
import os
import unittest


def _setup_inmemory():
    os.environ.setdefault("BOT_TOKEN", "test:test")
    os.environ.setdefault("ADMIN_TELEGRAM_ID", "1")
    os.environ["DATABASE_URL"] = "sqlite:///:memory:"


class _BaseCase(unittest.TestCase):
    @classmethod
    def setUpClass(cls):
        _setup_inmemory()
        import importlib
        from database import db as db_mod
        importlib.reload(db_mod)
        from database.models import Base
        Base.metadata.create_all(db_mod.engine)

        import services.delivery_service as ds_mod
        importlib.reload(ds_mod)
        cls.ds = ds_mod

    def setUp(self):
        from database import get_db_session, User
        with get_db_session() as s:
            user = User(telegram_id=9000 + id(self) % 100000, username="buyer",
                       wallet_balance=0.0)
            s.add(user)
            s.commit()
            self.user_id = user.id

    def _make_product(self, product_type, **kwargs):
        from database import get_db_session, Product
        with get_db_session() as s:
            p = Product(name=f"P-{product_type.value}", price=9.99,
                       stock_count=0, product_type=product_type, is_active=True,
                       **kwargs)
            s.add(p)
            s.commit()
            return p.id

    def _make_order_with_item(self, product_id, quantity=1, total=9.99):
        from database import get_db_session, Order, OrderStatus, OrderItem
        with get_db_session() as s:
            o = Order(user_id=self.user_id, total_amount=total, status=OrderStatus.PROCESSING)
            s.add(o)
            s.flush()
            item = OrderItem(order_id=o.id, product_id=product_id, quantity=quantity, price=9.99)
            s.add(item)
            s.commit()
            return o.id, item.id


class DownloadableFileDeliveryTest(_BaseCase):
    def test_delivers_telegram_file_id(self):
        from database import ProductType
        product_id = self._make_product(ProductType.DOWNLOADABLE_FILE,
                                        telegram_file_id="FILEID123",
                                        telegram_file_type="document")
        order_id, _item_id = self._make_order_with_item(product_id)
        result = self.ds.dispatch(order_id)
        self.assertTrue(result.handled)
        self.assertTrue(result.success)
        self.assertEqual(result.assets, ["FILEID123"])
        self.assertIn("document attached above", result.user_message)

    def test_missing_file_and_link_returns_error(self):
        from database import ProductType
        product_id = self._make_product(ProductType.DOWNLOADABLE_FILE)
        order_id, _item_id = self._make_order_with_item(product_id)
        result = self.ds.dispatch(order_id)
        self.assertTrue(result.handled)
        self.assertFalse(result.success)
        self.assertIn("no telegram_file_id", result.error)

    def test_non_reusable_file_is_idempotent_on_replay(self):
        from database import ProductType
        product_id = self._make_product(ProductType.DOWNLOADABLE_FILE,
                                        download_link="https://example.com/file.zip",
                                        reusable=False)
        order_id, _item_id = self._make_order_with_item(product_id)
        first = self.ds.dispatch(order_id)
        self.assertTrue(first.success)
        self.assertFalse(first.idempotent_replay)

        second = self.ds.dispatch(order_id)
        self.assertTrue(second.success)
        self.assertTrue(second.idempotent_replay)


class AutoGeneratedDeliveryTest(_BaseCase):
    def test_generates_requested_quantity_of_values(self):
        from database import ProductType
        product_id = self._make_product(ProductType.AUTO_GENERATED,
                                        type_config='{"mode": "code", "length": 10}')
        order_id, _item_id = self._make_order_with_item(product_id, quantity=3)
        result = self.ds.dispatch(order_id)
        self.assertTrue(result.handled)
        self.assertTrue(result.success)
        self.assertEqual(len(result.assets), 3)
        self.assertEqual(len(set(result.assets)), 3, "generated values must be unique")

    def test_replay_returns_same_values_without_regenerating(self):
        from database import ProductType
        product_id = self._make_product(ProductType.AUTO_GENERATED,
                                        type_config='{"mode": "code", "length": 10}')
        order_id, _item_id = self._make_order_with_item(product_id, quantity=2)
        first = self.ds.dispatch(order_id)
        second = self.ds.dispatch(order_id)
        self.assertTrue(second.idempotent_replay)
        self.assertEqual(sorted(first.assets), sorted(second.assets))

    def test_prefix_and_suffix_applied(self):
        from database import ProductType
        product_id = self._make_product(
            ProductType.AUTO_GENERATED,
            type_config='{"mode": "code", "length": 8, "prefix": "GC-", "suffix": "-X"}',
        )
        order_id, _item_id = self._make_order_with_item(product_id, quantity=1)
        result = self.ds.dispatch(order_id)
        self.assertTrue(result.success)
        value = result.assets[0]
        self.assertTrue(value.startswith("GC-"))
        self.assertTrue(value.endswith("-X"))


class ManualDeliveryTest(_BaseCase):
    def test_creates_queued_task_once(self):
        from database import ProductType
        product_id = self._make_product(ProductType.MANUAL_DELIVERY)
        order_id, _item_id = self._make_order_with_item(product_id, quantity=2)

        first = self.ds.dispatch(order_id)
        self.assertTrue(first.handled)
        self.assertTrue(first.queued)
        self.assertFalse(first.idempotent_replay)
        self.assertIn("queued for manual delivery", first.user_message)

        second = self.ds.dispatch(order_id)
        self.assertTrue(second.queued)
        self.assertTrue(second.idempotent_replay)

        from database import get_db_session
        from database.models import ManualDeliveryTask
        with get_db_session() as s:
            tasks = s.query(ManualDeliveryTask).filter_by(order_id=order_id).all()
        self.assertEqual(len(tasks), 1, "repeat dispatch must not create a second task")


